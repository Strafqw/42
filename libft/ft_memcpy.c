#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!dst && !src)
		return (NULL);
	if (d == s || n == 0)
	{
		return (dst);
	}
	while (n--)
		*d++ = *s++;
	return (dst);
}
